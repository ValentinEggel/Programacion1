#4-Calcular el sueldo de un operario, conociendo la cantidad de horas que trabajo en el mes y el precio por hora.

horas= int(input("Ingrese las horas trabajadas: "))
precio= float(input("Ingrese valor de la hora: "))
sueldo= (horas*precio)
print("El sueldo del operario es de: $ ", sueldo , " pesos.")