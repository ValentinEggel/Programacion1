#9-Construir un algoritmo que me permita leer un numero entero y determinar si este es mayor que 20.

valor= int(input("Ingrese un valor para evaluar: "))
if valor>20:
    print("El valor ingresado es mayor a 20")
elif valor==20:
    print("El valor ingresado es 20")
else:
    print("El valor ingresado es menor que 20")
